
# True = 1, False = 0
# None type
x = True
if x: 
    print(True)

x = -2 # x any value other than 0
if x: 
    print(True)


x = 0 # x any value other than 0
if x: 
    print(True)
else: 
    print(False)

x = 0 # x any value other than 0
if not x: # inverse
    print(True)
else: 
    print(False)

